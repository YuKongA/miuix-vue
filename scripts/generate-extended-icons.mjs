// Generates src/icons/extended/icons.ts from the miuix extended icon pack SVGs
// (sibling repo, per CLAUDE.md: D:\GitHub\miuix\). The pack is 155 icons × 5
// weights (Light/Normal/Regular/Medium/Demibold). Every SVG is a single path in
// its own square viewBox, fill-rule nonzero, with a vertical-flip transform
// matrix(1 0 0 -1 0 vh) — all uniform across the pack — so we store only
// { vw, vh, d } and the renderer (MiuixIcon) applies the flip + fill-rule.
//
// Run: bun scripts/generate-extended-icons.mjs

import { readdirSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { join } from 'node:path'

const SRC = 'D:/GitHub/miuix/docs/public/icons/extended'
const OUT_DIR = 'src/icons/extended'
const OUT = join(OUT_DIR, 'icons.ts')

const WEIGHT_MAP = {
  Light: 'light',
  Normal: 'normal',
  Regular: 'regular',
  Medium: 'medium',
  Demibold: 'demibold',
}
const WEIGHTS = ['light', 'normal', 'regular', 'medium', 'demibold']

const warnings = []
const icons = Object.create(null) // name -> { weightKey -> {vw,vh,d} }

for (const f of readdirSync(SRC)) {
  if (!f.endsWith('.svg')) continue
  const base = f.slice(0, -4)
  const dot = base.lastIndexOf('.')
  const name = base.slice(0, dot)
  const weight = WEIGHT_MAP[base.slice(dot + 1)]
  if (!weight) {
    warnings.push(`unknown weight in ${f}`)
    continue
  }
  const svg = readFileSync(join(SRC, f), 'utf8')
  const vb = (svg.match(/viewBox="([^"]+)"/) || [])[1] || ''
  const p = vb.trim().split(/\s+/).map(Number)
  const vw = p[2]
  const vh = p[3]
  const d = (svg.match(/\sd="([^"]+)"/) || [])[1] || ''
  const transform = (svg.match(/transform="([^"]+)"/) || [])[1] || ''
  const tn = ((transform.match(/matrix\(([^)]+)\)/) || [])[1] || '').trim().split(/\s+/).map(Number)
  // Assert the uniform vertical-flip matrix(1 0 0 -1 0 vh) the renderer assumes.
  if (
    tn.length !== 6 ||
    tn[0] !== 1 ||
    tn[1] !== 0 ||
    tn[2] !== 0 ||
    tn[3] !== -1 ||
    tn[4] !== 0 ||
    Math.abs(tn[5] - vh) > 0.5
  ) {
    warnings.push(`non-standard transform "${transform}" (vh=${vh}) in ${f}`)
  }
  if (!d || !vw || !vh) {
    warnings.push(`missing path/viewBox in ${f}`)
    continue
  }
  ;(icons[name] || (icons[name] = {}))[weight] = { vw, vh, d }
}

const names = Object.keys(icons).sort()
for (const n of names) {
  for (const w of WEIGHTS) if (!icons[n][w]) warnings.push(`${n} missing weight ${w}`)
}

const esc = (s) => s.replace(/\\/g, '\\\\').replace(/"/g, '\\"')
const wd = (w) => `{ vw: ${w.vw}, vh: ${w.vh}, d: "${esc(w.d)}" }`

let out = `// AUTO-GENERATED by scripts/generate-extended-icons.mjs — DO NOT EDIT.
// miuix extended icon pack: ${names.length} icons × 5 weights. Each weight is a
// single path in its own viewBox; the renderer applies the vertical-flip
// transform matrix(1 0 0 -1 0 vh) and fill-rule nonzero (uniform across the pack).
/* eslint-disable */

export interface ExtendedIconWeightData {
  /** viewBox width. */
  vw: number
  /** viewBox height (also the flip transform's ty). */
  vh: number
  /** path data. */
  d: string
}

export type MiuixIconWeight = 'light' | 'normal' | 'regular' | 'medium' | 'demibold'

export interface ExtendedIcon {
  name: string
  light: ExtendedIconWeightData
  normal: ExtendedIconWeightData
  regular: ExtendedIconWeightData
  medium: ExtendedIconWeightData
  demibold: ExtendedIconWeightData
}

`

for (const n of names) {
  const i = icons[n]
  out +=
    `export const ${n}: ExtendedIcon = { name: "${n}", ` +
    `light: ${wd(i.light)}, normal: ${wd(i.normal)}, regular: ${wd(i.regular)}, ` +
    `medium: ${wd(i.medium)}, demibold: ${wd(i.demibold)} }\n`
}

out += `\nexport const allExtendedIcons: ExtendedIcon[] = [\n  ${names.join(',\n  ')},\n]\n`

mkdirSync(OUT_DIR, { recursive: true })
writeFileSync(OUT, out)
console.log(`Generated ${names.length} icons → ${OUT}`)
if (warnings.length) {
  console.log(`WARNINGS (${warnings.length}):`)
  warnings.slice(0, 25).forEach((w) => console.log('  - ' + w))
}
